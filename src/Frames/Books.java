    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
      
        if((Validation.isEmpty(txtISBN.getText()) == false) && (Validation.isEmpty(txtTitle.getText())==false)&&(Validation.isEmpty(txtAuthorname.getText())==false)&&(Validation.isEmpty(txtEdition.getText())==false)&&(Validation.isEmpty(txtQuantity.getText())==false)&&(Validation.isEmpty(txtQuantity.getText())==false))
        {
            newBook.Add(txtISBN.getText(),txtTitle.getText(),cmbCatagory.getSelectedItem().toString(),txtAuthorname.getText(),txtEdition.getText(),txtQuantity.getText(),txtQuantity.getText());
            newBook.tableLoad();
        }
        else
        {
             JOptionPane.showMessageDialog(null,"Please Fill all the fields","Error",JOptionPane.ERROR_MESSAGE);

        }
        clear();
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        
        int msg =  JOptionPane.showConfirmDialog(null,"Do you really want to UPDATE this record?"); 
        
        if(msg ==0 )
        {
            try{
            
        int getSelectedRow = tblBooks.getSelectedRow();
        String BookID = tblBooks.getValueAt(getSelectedRow,0).toString();
       
          if((Validation.isEmpty(txtISBN.getText())== false)&&(Validation.isEmpty(txtTitle.getText())== false)&&(Validation.isEmpty(txtAuthorname.getText())== false)&&(Validation.isEmpty(txtEdition.getText())== false)&&(Validation.isEmpty(txtQuantity.getText())== false)&&(Validation.isEmpty(txtQuantity.getText())== false))
        {
            newBook.updateTable(BookID,txtISBN.getText(),txtTitle.getText(),cmbCatagory.getSelectedItem().toString(),txtAuthorname.getText(),txtEdition.getText(),txtQuantity.getText(),txtQuantity.getText());
            newBook.tableLoad();
        }
          else
        {
            JOptionPane.showMessageDialog(null,"Please Fill all the fields","Error",JOptionPane.ERROR_MESSAGE);
        }
        }
        catch(Exception e)
        {
            JOptionPane.showConfirmDialog(null, e);
        }
       clear();
        }
       
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        //Deleting data of the Table
        
          int msg = JOptionPane.showConfirmDialog(null, "Do you really want to DELETE this record?");
          
            if (msg == 0) 
            {
                int getSelectedRow = tblBooks.getSelectedRow();
            String BookID = tblBooks.getValueAt(getSelectedRow,0).toString();
        
                newBook.delete(BookID);
                newBook.tableLoad();
       
                clear();
            }
        
        
      
    }//GEN-LAST:event_btnDeleteMouseClicked

