    private void tblNewspapersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNewspapersMouseClicked
        // TODO add your handling code here:
                 int getSelectedRow = tblNewspapers.getSelectedRow();
         
       // String MagazineId = tblNewspapers.getValueAt(getSelectedRow, 0).toString();
        String 	Name= tblNewspapers.getValueAt(getSelectedRow, 1).toString();
        String NumofCopies=tblNewspapers.getValueAt(getSelectedRow, 2).toString();
        String 	SubscriptionType= tblNewspapers.getValueAt(getSelectedRow, 3).toString();
        String IssueDate= tblNewspapers.getValueAt(getSelectedRow, 4).toString();
        String Category= tblNewspapers.getValueAt(getSelectedRow, 5).toString();
       
        txtName.setText(Name);
        txtNoOfCopies.setText(NumofCopies);
       txtSubscriptionType.setText(SubscriptionType);
       
       cmbCategory.setSelectedItem(Category);
       String day = IssueDate.substring(0,2);
       cmbDay.setSelectedItem(day);
       
       String month = IssueDate.substring(3,5);
       cmbMonth.setSelectedItem(month);
       
       String year = IssueDate.substring(6,10);
       cmbYear.setSelectedItem(year);
    }//GEN-LAST:event_tblNewspapersMouseClicked

