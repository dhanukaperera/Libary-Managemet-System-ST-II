    private void btnUpdateMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseReleased
        // TODO add your handling code here:
            int msg =  JOptionPane.showConfirmDialog(null,"Do you really want to UPDATE this record?");
        
        if(msg==0)
        {
            int getRowId =  tblNewspapers.getSelectedRow();
         
            String PaperId =  tblNewspapers.getValueAt(getRowId, 0).toString();
        
            newnewspapers.updateTable(PaperId,txtName.getText(),txtNoOfCopies.getText(),txtSubscriptionType.getText(),cmbDay.getSelectedItem().toString(),cmbMonth.getSelectedItem().toString(),cmbYear.getSelectedItem().toString(), cmbCategory.getSelectedItem().toString());
        }
        clear();
    }//GEN-LAST:event_btnUpdateMouseReleased

