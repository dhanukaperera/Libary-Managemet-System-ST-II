    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        
       if((Validation.isEmpty(txtMemberId.getText()) == false)&&(Validation.isEmpty(txtMemberName.getText()) == false)&&(Validation.isEmpty(txtBookId.getText()) == false)&&(Validation.isEmpty(txtBookTitle.getText()) == false))
       {
          
            newlogicissuebooks.Add(txtMemberId.getText(),txtMemberName.getText(),txtBookId.getText(),txtBookTitle.getText(),cmbDay.getSelectedItem().toString(),cmbMonth.getSelectedItem().toString(),cmbYear.getSelectedItem().toString(),cmbDay1.getSelectedItem().toString(),cmbMonth1.getSelectedItem().toString(),cmbYear1.getSelectedItem().toString());
            newlogicissuebooks.tableLoad();       
         
       }
       else
       {
           JOptionPane.showMessageDialog(null,"Please Fill all the fields","Error",JOptionPane.ERROR_MESSAGE);
       }
       
       clear();
    }//GEN-LAST:event_btnAddMouseClicked

