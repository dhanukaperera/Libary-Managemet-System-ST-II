    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
         int msg =  JOptionPane.showConfirmDialog(null,"Do you really want to UPDATE this record?");
        
        if(msg==0)
        {
            int getRowId =  tblIssueBooks.getSelectedRow();
         
            String IssueBookId  =  tblIssueBooks.getValueAt(getRowId, 0).toString();
        
            newlogicissuebooks.updateTable(IssueBookId,txtMemberId.getText(),txtMemberName.getText(),txtBookId.getText(), txtBookTitle.getText(),cmbDay.getSelectedItem().toString(),cmbMonth.getSelectedItem().toString(),cmbYear.getSelectedItem().toString(),cmbDay1.getSelectedItem().toString(),cmbMonth1.getSelectedItem().toString(),cmbYear1.getSelectedItem().toString());
            clear();
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

