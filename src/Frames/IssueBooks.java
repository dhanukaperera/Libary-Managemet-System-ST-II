    private void tblIssueBooksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblIssueBooksMouseClicked
        // TODO add your handling code here:
               int getSelectedRow = tblIssueBooks.getSelectedRow();
         
     
        String 	MemberId= tblIssueBooks.getValueAt(getSelectedRow, 1).toString();
        String MemberName=tblIssueBooks.getValueAt(getSelectedRow, 2).toString();
        String 	BookID= tblIssueBooks.getValueAt(getSelectedRow, 3).toString();
        String BookTitle= tblIssueBooks.getValueAt(getSelectedRow, 4).toString();
        String IssueDate= tblIssueBooks.getValueAt(getSelectedRow, 5).toString();
        String DueDate= tblIssueBooks.getValueAt(getSelectedRow, 6).toString();
       
       
       txtMemberId.setText(MemberId);
       txtMemberName.setText(MemberName);
       txtBookId.setText(BookID);
       txtBookTitle.setText(BookTitle);
       
       String day = IssueDate.substring(0,2);
       cmbDay.setSelectedItem(day);
       
       String month = IssueDate.substring(3,5);
      cmbMonth.setSelectedItem(month);
       
       String year = IssueDate.substring(6,10);
      cmbYear.setSelectedItem(year);
      
      String day1 = DueDate.substring(0,2);
       cmbDay1.setSelectedItem(day1);
       
       String month1 = DueDate.substring(3,5);
      cmbMonth1.setSelectedItem(month1);
       
       String year1 = DueDate.substring(6,10);
      cmbYear1.setSelectedItem(year1);
       
       
    }//GEN-LAST:event_tblIssueBooksMouseClicked

