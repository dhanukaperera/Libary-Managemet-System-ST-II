    private void tblAddAccountTypeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAddAccountTypeMouseClicked
         int getSelectedRow = tblAddAccountType.getSelectedRow();

       
        String Type= tblAddAccountType.getValueAt(getSelectedRow, 1).toString();
        String Books= tblAddAccountType.getValueAt(getSelectedRow, 2).toString();
        String CD_DVD= tblAddAccountType.getValueAt(getSelectedRow, 3).toString();
        String NewsPapers= tblAddAccountType.getValueAt(getSelectedRow, 4).toString();
        String Magazines= tblAddAccountType.getValueAt(getSelectedRow, 5).toString();
        String Members= tblAddAccountType.getValueAt(getSelectedRow, 6).toString();
        String IssueBooks= tblAddAccountType.getValueAt(getSelectedRow, 7).toString();
        String ReturnBooks= tblAddAccountType.getValueAt(getSelectedRow, 8).toString();
        String Staff= tblAddAccountType.getValueAt(getSelectedRow, 9).toString();
        String Categories= tblAddAccountType.getValueAt(getSelectedRow, 10).toString();

        clear();
        
        txtType.setText(Type);
        
        
         
        
        
          if(Books.equals("Allow")){
           chkBooks.setSelected(true);
          }
        
         if(CD_DVD.equals("Allow")){
           chkCDDVD.setSelected(true);
         }
         
         if(NewsPapers.equals("Allow")){
           chkNewsPapers.setSelected(true);
         } 
  
         if(Magazines.equals("Allow")){
           chkMagazines.setSelected(true);
         }
         
         if(Members.equals("Allow")){
           chkMembers.setSelected(true);
         }
         
         if(IssueBooks.equals("Allow")){
           chkIssueBooks.setSelected(true);
         }
         
         if(ReturnBooks.equals("Allow")){
           chkReturnBooks.setSelected(true);
         }
         
         if(Staff.equals("Allow")){
           chkStaff.setSelected(true);
         }
        
         if(Categories.equals("Allow")){
           chkCategories.setSelected(true);
         }
        
         
         
      
        
            
            
        
        
      
         
         
         
    }//GEN-LAST:event_tblAddAccountTypeMouseClicked

